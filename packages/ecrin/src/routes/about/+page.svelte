<h1>A propos d'ECRIN</h1>

<p>
  ECRIN (European Clinical Research Infrastructure Network) est une infrastructure de recherche
  europeenne distribuee qui soutient les essais cliniques multinationaux.
</p>

<h2>Notre mission</h2>
<p>
  Faciliter la recherche clinique en Europe en fournissant des outils et services permettant aux
  chercheurs de mener des etudes de haute qualite dans le respect des normes ethiques et
  reglementaires.
</p>

<h2>Cette plateforme</h2>
<p>Ce dashboard permet aux chercheurs autorises de :</p>
<ul>
  <li>Consulter et gerer les donnees de recherche</li>
  <li>Suivre l'avancement des etudes</li>
  <li>Exporter des rapports</li>
  <li>Collaborer de maniere securisee</li>
</ul>

